/**
 * Режим источника транспортных данных
 * 
 * Определяет происхождение и качество данных в системе:
 * - REAL: данные получены из реального источника (OData), высокое качество
 * - RECOVERY: данные частично восстановлены автоматически, среднее качество
 * - MOCK: демонстрационные данные, используются при недоступности реального источника
 */

export enum DataSourceMode {
  /**
   * Реальные данные из OData API
   * Качество >= 90%
   */
  REAL = 'real',

  /**
   * Частично восстановленные данные
   * Качество 50-89%
   */
  RECOVERY = 'recovery',

  /**
   * Демонстрационные данные (fallback)
   * Используется при недоступности OData или качестве < 50%
   */
  MOCK = 'mock',

  /**
   * Неизвестный источник данных
   * Используется при ошибках загрузки данных
   */
  UNKNOWN = 'unknown',
}

